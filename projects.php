<p>In the first phase of the project all theoretically possible structures of a RuCp*-HBC complex will be modeled in order to determine the structure that is lowest in energy. In the next step these findings will be corroborated through conformational searches to determine the influence of the position that the side chains of the molecules might have on the total stability. Those results will then be used to adjust the initial calculations and a final data set will be produced.</p>
<p>This final data set of structural information will then be subjected to a range of analytical tools in order to determine the characteristics of the chemical bonding that are responsible for directing the metal atoms to the binding sites that are found experimentally. The most prominent analytical tools will be the <a href="http://en.wikipedia.org/wiki/Atoms_in_molecules">QTAIM</a> method that uses an analysis of the topology of the electron density for a description of the bonding features and the NBO (Natural Bonding Analysis) that uses localized orbitals for a Lewis-structure like description of the bonding situation. The combination of these two methods ensures a complete and unbiased picture of the compounds in question.&nbsp;</p>
