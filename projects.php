</tr></tbody></table><p></p></article><article class="project"><br><h4>Accelerating bioactive metabolite discovery through molecular modelling</h4>
<p>The approach taken is to build homology models of the 3D protein structures of the novel adenylation domains. Ligand docking software is then used to dock potential amino acid substrates into these homology models. The docked amino acids need to be ranked by binding affinity to predict which is the true substrate of the adenylation domain. However, while able to accurately predict the binding modes of the amino acids in the binding site, the ligand docking software does not accurately rank them.&nbsp;</p>
<p>In many cases, molecular dynamics simulations have been shown to be a more accurate computational method of predicting the binding affinity of small molecules bound to proteins. Hence, molecular dynamics simulations are used to perform post-processing of the results from the ligand docking of the amino acids into the adenylation domains. This should result in a better ranking of their binding affinity and allow us to predict the specificity of those adenylation domains.&nbsp;</p>
<p>Molecular dynamics simulations are complex, with a large number of parameters that need to be optimised. In addition, there are a number of different approaches to estimating binding affinity using molecular dynamics simulations. The project team aims to optimise the simulation parameters and test a number of different binding affinity estimation methods. &nbsp;Adenylation domains with experimentally-determined specificity will be used to do this. If this effort is successful, the team will be able to employ the method to predict the specificities of novel adenylation domains with no known specificity.</p>
