						<p>The University of Canterbury’s BlueFern supercomputer facilities have been helping Kiwi researchers tackle some big scientific problems.</p>
						<blockquote>
							<p>Without this fantastic resource, high resolution modelling of large ice sheet domains would be impossible.</p>
						</blockquote>
						<p>Among them is a Dr Nick Golledge from the Antarctic Research Centre at Victoria University of Wellington (VUW). His research team got one million free hours of use of the BlueFern supercomputer facility as part of the BlueFern  High Performance Computing (HPC) Grand Challenge. The challenge was established–as BlueFern Director Professor Tim David  puts it – to “turbocharge New Zealand science”.</p>
						<p>The project Dr Golledge leads utilised UC’s BlueGene L supercomputer to run a 3D ice sheet model to analyse changes in  the glaciers of New Zealand’s Southern Alps.</p>
						<p>Dr Golledge said the research aimed to “simulate at high spatial resolution the likely extent and behaviour of glaciers  25000 years ago, when the existing valley glacier system expanded into an extensive ice field at the height of the  Last Glacial Maximum”.</p>
						<p>“Using geological data to constrain the model, we have been able to acquire new insights into the ways that the present day environment changed during the last ice age, and from that, we can learn important lessons regarding the future of New Zealand’s glaciers under predicted future climate scenarios.”</p>
						<p>Dr Golledge said the ongoing research would have direct consequences for water resource management as well as for the tourism industry.</p>
						<p>“By winning the Grand Challenge award and having unique and invaluable access to the University of Canterbury BlueFern supercomputer for a million processor hours, we have been able to run our simulations at a higher resolution, and for longer periods, than ever before. Without this fantastic resource, high resolution modelling of large ice sheet domains would be impossible.”</p>
						<p>“The numerical code we were using, originally developed at the University  of Fairbanks, Alaska, has been specifically written for massively parallel architectures, and scaled well to the 4096 cores on BlueFern.”</p>
						<p>Dr Golledge said that to date, the model runs have shown that Southern Alps glaciers during the last glacial period were incredibly sensitive to temperature changes.</p>
						<p>“Even as little as a degree or two meant the difference between extensive glaciers that flowed through the major valleys, and ones that were only able to survive in much colder and higher mountain areas. Importantly, these experiments have also shown that the present-day glaciers are similarly sensitive.”</p>
						<p>Using the model to look ahead using climate predictions for the period 2000-2100, preliminary results suggested that the likely future warming of New Zealand’s climate is probably too great to be offset by the related precipitation changes, which commonly accompany rising temperatures.</p>
						<p>“Consequently it seems inevitable that New Zealand’s glaciers will shrink over coming decades, if the atmospheric temperature predictions of global climate models are borne out.”</p>
						<p>Dr Golledge said that the research team had further work to do with its models in order to better understand our glacier systems “and with invaluable tools such as the UC BlueFern currently underpinning e-Research in New Zealand, we hope to be able to build on these initial findings over the coming months and years”.</p>
