</tr></tbody></table><h3>Scientific applications</h3>
<p>Facilities install and maintain a wide variety of computational science applications and packages to support your research, though only for applications suitable to be run on HPC facilities. Licences are available for some application packages, while in other cases licences are required to be provided by the researcher.</p>
<h3>Capability and Capacity Computing</h3>
<p>Capability computing established a balance of computing resources, across memory, CPU cores, process interconnect, and data network, to provide a well tuned and matched optimum performance for a certain class of computational workload. Often such a system can solve computational problems far faster than any system without similar balance.</p>
<p>Capacity computing is far less optimised for specific classes of workload, rather it employs cost effective commodity componentry to deliver more general-purpose systems. These general-purpose systems are sometimes tuned and configured for larger workloads, and often also simply used for many tasks, or to aid in preparation for use of capability computing.</p>
<h3>Fit-for-Purpose</h3>
<p>A measure of Fit-for-Purpose is sometimes applied when assessing the match between computational workload and computing system, especially when looking to make optimal use of capability computing systems. With the expense required to create capability systems, this approach is applied in order that the most suitable workloads make efficient use of the balance of such an expensive research instrument.</p>
<h3>Allocation of HPC facilities</h3>
<p>NeSI provides an equal opportunity merit based allocation mechanism for any researcher working at a New Zealand public sector research institute or in private industry.</p>
