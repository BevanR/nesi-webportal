					<div class="entry">

					<p>The New Zealand eScience Infrastructure (NeSI) received applications for about 700,000 CPU core hours in its first call for access proposals from New Zealand researchers. This represents the first of many steps to improving access to high performance computers for Kiwi researchers, and enhancing the environment for computing intensive science in New Zealand.</p>

					<p>Applications currently being reviewed span many fields and institutions, with topics including:</p>
					<ul>
						<li>genomics to support biosecurity,</li>
						<li>developing semiconductor gas sensors,</li>
						<li>climate modelling,</li>
						<li>simulating airflow for wind turbines,</li>
						<li>understanding enzymes that support animal and human </li><li>health,</li>
						<li>exploring the genetic history of the Influenza virus, </li><li>and</li>
						<li>cancer drug evaluation</li>
					</ul>
					<p>NeSI is a national research infrastructure investment supporting computing intensive research in New Zealand. Its primary assets are supercomputers, hosted in three high performance computing (HPC) facilities. The facilities are hosted at <a href="#">The University of Auckland</a>, the <a href="#">University of Canterbury</a> and <a href="#">NIWA</a>.</p>

					<p>HPC enables computing intensive research to operate at larger scales, finer resolutions, and over shorter time frames. Comments within the applications suggest that researchers value access to these HPC facilities very highly. One project team asserted that “Access to computational cluster facilities is essential”, which was is echoed in another project that stated “without this access, such studies, ... cannot be completed.” We wish the successful researchers all the best with their projects.</p>

					<p>NeSI welcomes contact from all New Zealand researchers from all disciplines and institutions seeking support for their computing intensive research needs. The next call for large projects is in July and every two months during this first year. Smaller and evaluation projects can be requested at any time.</p>
